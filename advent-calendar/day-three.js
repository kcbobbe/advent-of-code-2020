var splitRows = path.split('\n')

function slopePath(right, down) {
    var treeCount = 0;
    var column = 0;
    for (let row = 0; row < (splitRows.length); row+=down) {
        if(splitRows[row][column] === '#') {
            treeCount++
        }
        column += right;
        if (column >= (splitRows[0].length)) {
            column = column - (splitRows[0].length)
        }
    }
    return treeCount;
}
